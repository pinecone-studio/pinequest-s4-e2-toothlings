/** Зөвлөмжийн текстийг цэгэн жагсаалтын мөрүүд болгож задлана (• / - тэмдгийг цэвэрлэнэ). */
export const guidanceLines = (text: string): string[] =>
  text
    .split('\n')
    .map((l) => l.replace(/^\s*[•\-*]\s*/, '').trim())
    .filter(Boolean)

/** Текстийг цэгэн жагсаалт (олон мөр эсвэл • / - тэмдгээр эхэлсэн) гэж үзэх эсэх. */
export const isBulletedText = (text: string): boolean => /\n/.test(text) || /^\s*[•\-*]/.test(text.trim())

